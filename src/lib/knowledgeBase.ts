export function getKnowledgeDocuments(): string[] {
  // Diviser la base de connaissances en chunks pour le RAG
  const chunks = portfolioKnowledge
    .split('\n##')
    .map((chunk, index) => (index === 0 ? chunk : '##' + chunk))
    .filter((chunk) => chunk.trim().length > 0);
  
  return chunks;
}

